    @TableId(value = "id", type = IdType.AUTO)
    private Long id;

    /**
     * 角色编码：admin/operator/viewer
     */
    private String roleCode;

    /**
     * 角色名称
     */
    private String roleName;

    /**
     * 角色描述
     */
    private String description;

    /**
     * 状态：0禁用，1启用
     */
    private Integer status;

